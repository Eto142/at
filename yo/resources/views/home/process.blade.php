<div class="c-accordion__item js-acc-item is-active text-dark" style="background-color: #3653a7">
<div class="c-accordion__title-wrap">
<button class="js-acc-button c-accordion__title icons-arrows">
5% Max Daily Sim Loss </button>
</div>
<div class="js-acc-content">
<div class="c-accordion__content is-wysiwyg">
Traders are allowed a maximum of 5% daily drawdown on their sim account. This is calculated based on your day's starting balance and or equity depending on your challenge type and it is calculated at 5PM EST. Meaning that your sim account's equity should not at any point go below 94% of your day's starting balance and or equity.
For example, on the Regular and Swing account's if you had a balance and equity of $100,000.00 at 5PM EST, your daily drawdown limit would be $5,000.00. Meaning that your sim account's equity would not be allowed to go below $95,000.00. Whereas on the plus account if you had a sim balance of $100,000.00 and equity of $105,000.00 at 5PM EST, your daily drawdown limit would be $5,250.00 which is based on your equity as it is higher than the balance. Meaning that your account's sim equity would not be allowed to go below $99,750.00. </div>
</div>
</div>
<div class="c-accordion__item js-acc-item  text-dark" style="background-color: #ffa16c">
<div class="c-accordion__title-wrap">
<button class="js-acc-button c-accordion__title icons-arrows">
8%/10% Overall Virtual Loss </button>
</div>
<div class="js-acc-content">
<div class="c-accordion__content is-wysiwyg">
Your sim account equity may not at any point drop lower than 8% or 10% of your sim account's initial value depending on your challenge type.
For example, if you start with $100,000 and your max overall has is 8% ($8,000) then your sim account's equity should not at any point drop lower than $92,000. The same goes if your max overall loss is 10% ($10,000), meanings that your sim account's equity should not at any point drop lower than $92,000. </div>
</div>
</div>
<div class="c-accordion__item js-acc-item  text-dark" style="background-color: #3653a7">
<div class="c-accordion__title-wrap">
<button class="js-acc-button c-accordion__title icons-arrows">
8% / 10% Tier 1 Virtual Profit Target </button>
</div>
<div class="js-acc-content">
<div class="c-accordion__content is-wysiwyg">
Depending on your challenge type you would need to make 8% or 10% virtual profit in Tier 1 within 40-day period. Once successful, you will then advance to Tier 2.
For example, if you trade a $100,000 sim account on Regular and Swing challenge you would be required to make 10% ($10,000) virtual profit in Tier 1 before advancing to Tier 2. However, if it were a Plus challenge you would be required to make 8% ($8, 000) virtual profit in Tier 1 before advancing to Tier 2. </div>
</div>
</div>
<div class="c-accordion__item js-acc-item  text-dark" style="background-color: #ffa16c">
<div class="c-accordion__title-wrap">
<button class="js-acc-button c-accordion__title icons-arrows">
5% Tier 2 Virtual Profit Targets </button>
</div>
<div class="js-acc-content">
<div class="c-accordion__content is-wysiwyg">
Once in Tier 2 you would be required to make 5% virtual profit within a 60 or 70-day period depending on your challenge type. Once successful, you will then advance to TopTier which is the simulated funded phase.
For example, if you trade a $100,000 sim account, you would be required to make 5% ($5,000) virtual profits in Tier 2 before advancing to TopTier. </div>
</div>
</div>
<div class="c-accordion__item js-acc-item  text-dark" style="background-color: #3653a7">
<div class="c-accordion__title-wrap">
<button class="js-acc-button c-accordion__title icons-arrows">
Minimum Sim Trading Days </button>
</div>
<div class="js-acc-content">
<div class="c-accordion__content is-wysiwyg">
To pass the TopTier Trading Challenge, traders must trade a minimum of 4 days during the given trading periods. At least one simulated position must be opened on each of these days. This helps traders to sharpen their trading habits and make sure they are gaining in a consistent way. </div>
