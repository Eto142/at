                    <p class="lead-2 mb-3 fw-700">CFD stands for ‘Contract for Difference’ and is a contract between two parties agreeing to exchange the difference in the value of a security, instrument, or other asset between the time at which the CFD is opened and the time at which it is closed.</p>
                    <p class="lead-3 mb-5">CFDs are extremely versatile products growing in popularity as a short term investment tool. They provide an efficient way of maximizing your capital outlay and can help diversify your existing investment portfolio or hedge a position. Some of the advantages of trading CFDs are listed below.</p>
                    <div class="platform-box grey mb-4">
                        <h4 class="fw-700 mb-2">Speculate in Both Rising and Falling Markets</h4>
                        <p class="lead-4 mb-1"> CFDs are derivatives based on an underlying instrument. There is no ownership of the underlying asset, however they allow you to participate in the price movement of the asset. This means you can potentially profit in both rising and falling markets.</p>
                        <p class="lead-4 mb-1">In a rising market you would look to buy a CFD and then sell at a later date. This is called ‘going long’.</p>
                        <p class="lead-4 mb-3">In a falling market you would look to sell a CFD position first and then buy it back at a later date closing out the position. This is known as ‘going short’.</p>
                    </div>
                    <div class="platform-box grey mb-4">
                        <h4 class="fw-700 mb-2">Efficient Use of Capital</h4>
                        <p class="lead-4 mb-1">CFDs are leveraged products enabling traders to increase their exposure to an underlying asset with a small initial outlay. When you open a trade you only need to deposit a small percentage of the value of the position and this is known as margin. Your margin will vary depending on the value of your CFD position. Leverage can result in added gains should the market move in your favour, however it also carries risks and can result in increased looses should your position move against you.</p>
                    </div>
                    <div class="platform-box grey mb-4">
                        <h4 class="fw-700 mb-2">Hedging Other Investments</h4>
                        <p class="lead-4 mb-1"> The ability ‘go long’ as well as ‘go short’ with CFDs means that they are a great tool for hedging an existing portfolio. They are a cost effective alternative to selling the portfolio prematurely and can be used to provide ‘insurance’ against adverse price movements.</p>
                        <p class="lead-4 mb-1">For example, if you have a long-term portfolio that you wish to keep, however you are of the view that there is some short term risk to the value of the portfolio you could use CFDs to ‘hedge’ your positions. If the value of the portfolio falls the profit you make on the CFDs will offset the losses in your portfolio.</p>
                    </div>
                    <div class="platform-box grey mb-4">
                        <h4 class="fw-700 mb-2">Flexible Contract Sizes</h4>
                        <p class="lead-4 mb-1">The contract sizes of CFDs are often less than the typical contract size of the underlying instrument which means you can gain exposure to the price movement of the instrument without a significant deposit. Flexible sizing allows you to tailor your trading according to your risk management criteria.</p>
                    </div>
                    <div class="platform-box grey mb-4">
                        <h4 class="fw-700 mb-2">Access Global Financial Markets</h4>
